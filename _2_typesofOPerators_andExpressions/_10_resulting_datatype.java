public class _10_resulting_datatype {
    public static void main(String[] args) {
        /* resulting data types after operation
        * R = b + s --> int
        * R = s + i --> int
        * R = l + f --> float
        * R = i + f --> float
        * R = c + i --> int
        * R = c + s --> int
        * R = l + d --> double
        * R = f = d --> double

        b-> byte       f-> float
        s-> short      d-> double
        i-> integer    c-> character   l-> long   */

        byte x = 5;
        int y = 6;
    }
}
